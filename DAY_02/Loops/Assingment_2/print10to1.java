package Loops.Assingment_2;

public class print10to1 {
   public static void main(String[] args) {

    // FOR LOOP
    for( int i=10;i>0; i--)
    {
     System.out.println(i);
    }

  // WHILE LOOP
  int i=10;
  while(i>=1)
  {
    System.out.println(i);
    i--;
  }
  // DO WHILE LOOP
  i=10;
  do {
  System.out.println(i);
  i--;
 }while(i>=1);
} 
}